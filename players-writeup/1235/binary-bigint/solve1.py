from sympy import symbols, solve

l1 = [0x65, 0x2F, 0x2B, 0x0F, 0x0A, 0x6D, 0x23, 0x7D, 0x3D, 0x75, 0x73, 0x7C, 0x20, 0x57, 0x16, 0x33, 0x42, 0x23]
l2 = [0x01, 0x4C, 0x0C, 0x37, 0x00, 0x09, 0x07, 0x64, 0x20, 0x4C]
l3 = [0x10, 0x0A, 0x54, 0x2A, 0x3F, 0x72, 0x0C, 0x4E, 0x7E, 0x49, 0x1D, 0x46, 0x64, 0x44, 0x7E, 0x31, 0x41, 0x4A, 0x0E, 0x41, 0x69, 0x3C, 0x2A, 0x00, 0x29, 0x2D, 0x50]

def enc(l):
    res = 0
    for i in range(len(l)):
        res = res * 128 + l[i]
    return res

def dec(x):
    res = []
    while x > 0:
        res = [x % 128] + res
        x = x // 128
    return bytes(res)

a1 = symbols('a1')

c2 = -enc(l2)
c1 = enc(l1)
c0 = -enc(l3)

equation = a1**3 + c2*a1**2 + c1*a1 + c0
solutions = solve(equation, a1)
print(solutions)
for x in solutions:
    print(dec(x))